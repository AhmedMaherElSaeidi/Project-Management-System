    public PMProjectProgress() {
        initComponents();
        try {
            db = new DataBase(new File(this.path));
            String[] lines = db.splice(db.readLines(), 0);

            ArrayList<String> projects = new ArrayList<>();
            for (String line : lines) {
                String[] words = db.toFields(line);

                if (!projects.contains(words[2])) {
                    projects.add(words[2]);
                }
            }

            for (int i = 0; i < projects.size(); i++) {
                jComboBoxProject.addItem(projects.get(i));
            }
        } catch (IOException ex) {
            System.out.println(ex);
        }
    }

