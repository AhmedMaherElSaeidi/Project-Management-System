    private void jComboBoxProjectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBoxProjectActionPerformed
        String project = (String) jComboBoxProject.getSelectedItem();
        try {
            int total = 0;
            String[] lines = db.getMatchedRecordsWhere(2, project);

            for (String line : lines) {
                String[] words = db.toFields(line);
                if ("true".equals(words[4])) {
                    total++;
                }
            }

            double division = (total / 5.0) * 100;
            jTextFieldProject.setText("Progress Rate for project of ID : " + project + " is " + division + "%.");
            jProgressBar.setValue((int) Math.ceil(division));
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(null, ex, "Error", JOptionPane.ERROR_MESSAGE);
        }        // TODO add your handling code here:
    }//GEN-LAST:event_jComboBoxProjectActionPerformed

