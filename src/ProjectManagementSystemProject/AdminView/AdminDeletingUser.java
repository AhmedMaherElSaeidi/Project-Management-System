    private String collectData(String userID, String fileName, String[] info) throws IOException {
        db.setFile(new File(this.path + fileName));
        String data = info[0];

        if (db.recordExistsWhere(0, userID)) {
            String[] lines = db.getMatchedRecordsWhere(0, userID);

            for (int iterator = 0; iterator < lines.length; iterator++) {
                String[] words = db.toFields(lines[iterator]);
                data += "\t" + (iterator + 1) + ") " + String.join("\t", db.splice(words, 0)) + System.lineSeparator();
            }
        } else {
            data += info[1];
        }

        return data;
    }

