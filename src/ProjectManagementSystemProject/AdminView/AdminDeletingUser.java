    private void removeUserData(String fileName, String userID) throws IOException, Exception {
        db.setFile(new File(fileName));

        while (db.recordExistsWhere(0, userID)) {
            db.deleteRecordWhere(0, userID);
        }
    }


    private void jButtonOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonOkActionPerformed
        String userID = Admin.id;

        if (!userID.equals("")) {
            try {
                db.setFile(new File(this.path + "usersTable.txt"));
                String[] values = db.toFields(db.getRecordWhere(0, userID)[0]);

                String userName = values[1];
                db.deleteRecordWhere(0, userID);
                String[] filesName = {"PenalitiesTable.txt", "VaccationsTable.txt", "ReportsTable.txt", "TasksTable.txt", "EntryTimeTable.txt"};
                for (String fileName : filesName) {
                    removeUserData(this.path + fileName, userID);
                }

                JOptionPane.showMessageDialog(null, "Successfuly Deleted " + userName + " From the System.", "Message", JOptionPane.INFORMATION_MESSAGE);
                this.dispose();
            } catch (IOException ex) {
                Logger.getLogger(AdminDeletingUser.class.getName()).log(Level.SEVERE, null, ex);
            } catch (Exception ex) {
                Logger.getLogger(AdminDeletingUser.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            this.dispose();
        }
    }//GEN-LAST:event_jButtonOkActionPerformed

